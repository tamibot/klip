                                  carbonModifiers: UInt32(d.integer(forKey: K.mods7)))
        uiLanguage = d.string(forKey: K.uiLang) ?? "en"

        // One-time migration: the capture default changed from ⌘⇧2 to ⌘⇧U (⌘⇧2 was hijacked by apps like Loom).
